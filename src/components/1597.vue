				<br>
				<br> 她所经历过的一切痛苦，都化作了她脸上的云淡风轻，成为了一段不可遗忘的时光。
				<br>
				<img alt="" src="http://image.wufazhuce.com/FnG9QSD7ZfA8oTLcK-bZ3CppymFD?imageView2/2/w/394" style="height:296px; width:394px" /><br> 2016年，五十九岁的蔡琴在香港的演唱会上说了这样一段话——
