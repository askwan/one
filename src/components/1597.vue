				<br>
				<br> 可是蔡琴，却陷入了人生的低谷期。
				<br>
				<img alt="" src="http://image.wufazhuce.com/Fv1PkKTTUxkQqxbAUzAerCjPoE3M?imageView2/2/w/394" style="height:374px; width:394px" /><br> 她不愿意说话，不想和外人接触，对于音乐的热爱似乎在慢慢流逝。
