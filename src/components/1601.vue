	<img alt="" src="http://image.wufazhuce.com/FvguHpwpNYCShVjSZ5nyDSv9fZ67?imageView2/2/w/394" style="height:221px; width:394px" />
	<br>
我们将用这篇近4000字的干货与你一起回溯，小提琴这一传统乐器在摇滚音乐中的发展脉络，从“地下丝绒”看到 David Bowie，朋克摇滚再到融合爵士，这门古老的器乐技艺是如何勾勒出摇滚乐的变局，其中又有怎样的故事？<br>
<br>
<strong>John Cale：蹒跚的弦乐开场</strong><br>
<img alt="" src="http://image.wufazhuce.com/FtlyNDMhG9ANSSnuvMb9WnWc6ueE?imageView2/2/w/394" style="height:395px; width:394px" /><br> John Cale，“地下丝绒”的创始成员之一，他的经典形象定格在Andy Warhol一手创建的多媒体实验演出“塑料爆炸不可避免”中，那个站在Lou Reed身后不远处推拉着提琴琴弓，墨镜深黑的青年。他为“地下丝绒”经典的香蕉封面专辑《The Velvet Underground & Nico》献出了《The Black Angel’s Death Song》这样癫狂迷乱的弦乐开场。<br>
<br> 对待提琴，他手段粗野，把吉他和曼陀林的琴弦装到了琴颈上，再用他从La Monte Young身上学到的那一套“嗡鸣”（drones）技术，让这门古老的乐器发出了安非他命过量般的蹒跚音色。怪异如此，但他其实是理论派，煞有其事地分享他的那套弦乐概念。<br>
<br> “地下丝绒真正的音响来自于在恒音剧院里和La Monte Young的合作……我们发现用琴弓拉吉他会发出如此庞大的乐团式噪音。我们将这种方法用在了中提琴和小提琴上，接下来我卸下中提琴的琴桥，在三根弦上演奏……它制造出了庞大的噪音，听起来就像你屋子里有架飞机……Lou和我几乎是以宗教般的狂热去对待我们的工作——比如试着将La Monte Young或Andy Warhol的一些概念放进摇滚乐。”<br>
<br>
然而Lou Reed的狂热持续不过短短两年。《White Light/White Heat》那年，“地下丝绒”的粗野被这四个人一股脑地挤进那首17分钟之长的器乐大战《Ray Sister》里，John Cale这个理论派还是技高一筹，即使不握提琴，改弹奏风琴，照样碾压两把电吉他。整张专辑呈现一种可怕的飞升之姿，让内心事实上趋向安静温和的Lou Reed有点受不了。他开除了John Cale。<br>
<br>
听听《Pale Blue Eyes》，没有了这个人用那些改装过的提琴“搅场子”，“地下丝绒”之后的专辑实在是清爽太多了。<br>
<img alt="" src="http://image.wufazhuce.com/FijeBuoAlBX55H0FCxCDw5r_jMuP?imageView2/2/w/394" style="height:255px; width:394px" /><br>
离开“地下丝绒”，John Cale大变活人一般地在他的英国同胞，令人心碎的民谣歌手Nick Drake的专辑《Bryter Layter》中再次操起中提琴演奏了那首温顺到叫人肝颤的《Fly》。此人的尖端古典乐修养其实注定了他要比Lou Reed等人走得更远，能拉琴能唱歌。<br>
<br>
1973年的一张《Paris 1919》里他典雅如同巴洛克时代美男，后来却回头参与进CBGB的朋克场景，给Patti Smith制作专辑，各种抽身而走迅如闪电。他自己大概是没想到他在《The Velvet Underground & Nico》里拉出的那几下提琴音色已经够很多人吃半辈子了。<br>
<br>
并无史料证实John Cale是第一个在摇滚乐团里演奏中提琴和小提琴的人，这门古老的器乐技艺拥抱摇滚乐的姿势可谓跌跌撞撞，在Cale这儿连自己的原装琴弦都不让用，还得借吉他这个摇滚乐亲儿子的货。<br>
<br>
又或者它的加入只不过是乐队头脑风暴的产物，比如The Beatles发于1968年的经典“白砖”《The Beatles》，倒数第四首《Don’t Pass Me By》，请的是一个叫Jack Fallon的人来拉了一段小提琴。众所周知这张专辑里甲壳虫四人各怀鬼胎，概念一团乱，时至今日已经不可考是谁想到这个主意的了。这位Fallon留下来的料很少，据维基显示他的本职其实是一位爵士乐贝斯手。可见，早期的摇滚乐对于是否欢迎传统和职业的小提琴家参与是持怀疑态度的。<br>
<br>
<strong>Eddie Jobson：琴弓上展现的魅力</strong><br>
<img alt="" src="http://image.wufazhuce.com/FgKzvDWX9U0SXLm6tRcEguXfkDMO?imageView2/2/w/394" style="height:314px; width:394px" /><br>
对此怀疑的破局缓慢，但并非毫无成就。善于破坏旧世界的摇滚乐在60年代末期最重要的任务是要破坏自己的僵局，将猫王的白人摇滚和甲壳虫的默西之声音色彻底抹去，在电子音乐尚无任何萌芽迹象的年代，它唯一的方法反而是向旧世界借一点东西以拓宽音色的广度。<br>
<br>
接受这一任务的是最早的一批前卫摇滚先驱，Roxy Music和King Crimson构成双核。这两支长时间以来不断进行人员调整的前卫乐团凭借自身巨大的魅力，吸引又输出了一批批的优秀音乐家穿梭于60、70年代的英国摇滚乐坛，其中也包括了Eddie Jobson、David Cross这样出色的提琴演奏家。<br>
<img alt="" src="http://image.wufazhuce.com/FmrZSCgsBcJ8qsx_O138EDMFPLL2?imageView2/2/w/394" style="height:260px; width:394px" /><br>
Eddie Jobson，8岁学琴，早慧神童，他是前卫乐团Curved Air的领袖Darryl Way的后辈，接替Darryl Way成为乐队的第二任小提琴手。他的这位前辈是根正苗红的伦敦皇家音乐学院的科班生（Jobson也曾申请该学院，但因为申请时年龄差一岁而被拒绝），创造性地将维瓦尔第的“四季”带上了摇滚舞台。<br>
<br>
Curved Air这支短命的乐队为Eddie Jobson起到了跳板的作用，期间他顺利结识了Roxy Music的主唱Bryan Ferry。Ferry其人野心勃勃，是少数乐队如日中天之时就要推出个人solo专辑的主唱，这或多或少导致了Brian Eno早早地对Roxy Music产生厌倦之情。1973年Eno离队，Eddie Jobson以提琴手、键盘手、合成器三重身份加入Roxy Music。<br>
<br>
19岁的Eddie Jobson，瘦削靓丽，似乎比年少秃顶的Brain Eno更符合Roxy Music的外形定位。他在舞台上演奏他标志性的全透明小提琴，其精湛优雅深受乐坛瞩目，俘获一大票圈内粉丝。John Wetton，这位King Crimson乐团第三期阵容的贝斯手兼主唱在与Roxy Music合作时对他印象尤为深刻。<br>
<img alt="" src="http://image.wufazhuce.com/FsxeVRuvTLSSikXW4x59WgUt1W2h?imageView2/2/w/394" style="height:289px; width:394px" /><br>
King Crimson的大脑，前卫吉他之神Robert Fripp生性好动，思维跳跃，跟任何人都没办法长期合作，导致这支触角延伸数十年的超级天团除了他本人之外从来没有一个真正稳定的阵容。John Wetton担纲主唱贝斯手的第三期阵容也只持续到1975年。<br>
<br>
这个时期他们有David Cross这样一位实力超群的小提琴手/键盘手。此兄在《Lark’s Tongues in Aspic, Pt.2》中的演奏有一种强酸般的听觉腐蚀感，像琴弓直接扎进耳骨里产生的剧痛，引起不适。这一如次声波的提琴音响在KC制作现场专辑《USA》时产生了大问题，它直接在音轨中消失了。<br>
<br>
Eddie Jobson被Wetton请来救场，叠录了小提琴音效。这一时期的King Crimson，音乐思维杂乱，Fripp迷信世界末日传说，以至于阻滞乐队巡演，John Wetton和鼓手Bill Bruford试图力挽狂澜而不得，终至解散。<br>
<br>
Roxy Music和King Crimson的两大核心一个自负一个乖张，在旁人看来全都难搞至极，反倒是各自人员交流时更易惺惺相惜。1976年，明确KC和解无望的JohnWetton和Bill Bruford组建U.K.乐队，力邀Eddie Jobson入团担任提琴手。这一年Jobson从Roxy Music“毕业”，被Frank Zappa挖角到他的乐队里。<br>
<br>
奇怪的是，虽然Jobson的形象出现在Zappa当年的专辑《Zoot Allures》的封面上，这张专辑里却没有任何一段音源是由他演奏的。“他的东西总是提前就录好的。你可能进他的乐队的时候他就已经有一张做好的专辑了，也可能你离开他五年以后才发现他有张新专辑里有你的东西。”Jobson说，这叫人沮丧。长久以来这位小提琴天才徘徊于一线摇滚乐梯队中，积攒人脉，却始终没有一种归属感。当John Wetton的橄榄枝伸向他的时候，他毫不犹豫地接受了。<br>
<br>
<strong>Simon House：为巨星配色</strong><br>
<img alt="" src="http://image.wufazhuce.com/FvcD2ZoRujEcUW70Oaw1K9inDpNZ?imageView2/2/w/394" style="height:197px; width:394px" /><br>
很快会有一位重要人物再次将Roxy Music和King Crimson连接起来，他就是不久之前给“Ziggy Stardust”判下死刑的David Bowie。1978年，他在柏林彻底放下过往的荣耀，从头审视身为艺术家自我的灵魂，为此创作后世所谓的“柏林三部曲”专辑。从Roxy Music肄业的Brian Eno和处于休整期的Robert Fripp先后加入Bowie的制作阵容，诞下传世名曲《"Heroes"》中荡气回肠的吉他电气。<br>
<br>
《Low》和《"Heroes"》发行完毕后Bowie迅速展开巡演，他在舞台上以一袭工人衬衫或黑皮衣的新面目示人，意在重拾坚韧性情，无论是人设还是音乐上。Bowie也完全舍弃“火星蜘蛛”，为这一期巡演配备了全新的乐队：Frank Zappa的吉他副手Adrian Belew，新浪潮乐队Utopia键盘手Roger Powell，还有，Simon House这位出身于太空摇滚先锋乐队Hawkwind的小提琴手。<br>
<img alt="" src="http://image.wufazhuce.com/FlVCqwMpWyMYk9DpaqMAD0HsDIh0?imageView2/2/w/394" style="height:368px; width:394px" /><br>
Hawkwind走出了一位石破惊天的“摩托头”Lemmy，其贝斯雄风使得乐队原本酸性十足的氛围感转变为强风，一路沿革至1975年Mot&ouml;rhead诞生。他与Simon House短暂际会于Hawkwind1974年的专辑《Hall of Mountain Grill》中，后者华丽绵长的提琴演奏如坠云中，两人相衬，就是天与地遥遥对应，让Hawkwind一别Pink Floyd式的晦涩史诗，踩着华丽摇滚的尾巴，接上了萌芽状态中的重金属摇滚。<br>
<br>
Simon House在Bowie1978年的世界巡演中低调地站在舞台侧面，一如小提琴从来不曾在摇滚乐中喧宾夺主。他之后参与了“柏林三部曲”终章《Lodger》的制作，伴随Bowie完美收尾他的硬派时代。<br>
<br>
70年代末期Krautrock的横空出世多多少少影响到Bowie柏林时期的音乐理念，热衷于那些怪力乱神的合成器氛围，不过他对于弦乐的兴趣可能要直接追溯至1972年他与“地下丝绒”的初识，彼时双性外星人“Ziggy Stardust”真正见识到了美国地下文化之狂野，”地下丝绒是Cale的前卫思想和Lou Reed的流行乐表现手法的混合体”，虽然72年John Cale已经和“地下丝绒”无关，但Bowie强烈的仰慕之情，仍然表明了这位琴弓上的巨匠拥有怎样庞大的影响力。<br>
<br>
<strong>L.Shankar： 融合与跨界</strong><br>
<img alt="" src="http://image.wufazhuce.com/Fh_ujaQgjTVElIbv9Yz6b4A60c3e?imageView2/2/w/394" style="height:263px; width:394px" /><br>
纽约地下文化之野性，是凯鲁亚克、威廉·巴勒斯和艾伦·金斯堡王国中的遗存，令Bowie羡艳。Andy Warhol驱动着他的商业帝国将“地下丝绒”培植成消费和纵欲主义的文化符号，但也消弭了音乐的严谨。<br>
<br>
与纽约朋克的粗鲁敬意相对应的是，一是迷幻摇滚，“爱之夏”和盛大的伍德斯托克。旧金山的“杰斐逊飞机”在1972年分化成Hot Tuna和Jefferson Starship，带着布鲁斯背景的黑人小提琴家Papa John Creach未曾离弃过这个团队。<br>
<br>
二是以来自英国的吉他手John McLaughlin为代表的古朴型专业音乐家。他带着狂热的态度欣赏着法国著名小提琴家Jean-Luc Ponty精湛的爵士提琴技艺。Ponty创造性地为小提琴音色注入合成器和电子音乐效果，在与Frank Zappa合作的一首《King Kong》中喷薄出灼灼烈焰，让McLaughlin羡慕不已又技痒难耐，力邀他加入自己的融合爵士天团Mahavishnu Orchestra。<br>
<br>
两人有心携手但无奈重洋阻隔，不能如意，McLaughlin遂转向美国本土的小提琴家Jerry Goodman，后者，虽然履历尚浅，但终究没有让McLaughlin失望。71年首砖《The Inner Mounting Flame》小提琴几乎成为主打乐器，一举突破融合爵士乐定势。<br>
<img alt="" src="http://image.wufazhuce.com/FhWOkhkWaRyVT6_CDNVifVx09c7K?imageView2/2/w/394" style="height:340px; width:394px" /><br>
而McLaughlin风格转变之迅猛，丝毫不亚于John Cale的活跃跨界。Mahavishnu Orchestra借用了印度神话三相神之一的毗湿奴的名字，凸显东方神秘韵味，音符也夹带香料气息。1975年，McLaughlin干脆直接组建了一支专攻印度音乐的乐队，与他一道的是印度手鼓大师Zakir Hussain和小提琴家L.Shankar。<br>
<br>
L.Shankar技艺精湛，人缘极佳，他可以在与McLaughlin的协同中演奏出热风阵阵的快速弦乐线条，也可以和Peter Gabriel合作为电影《基督最后的诱惑》献出配乐，在整个70、80年代非常活跃，合作名单上至Elton John，下至Talking Heads。<br>
<br>
<strong>Owen Pallett：新时代的玩法</strong><br>
<img alt="" src="http://image.wufazhuce.com/FkzPMyYqUtU1f6IoVtnJZLWBD073?imageView2/2/w/394" style="height:221px; width:394px" /><br>
79年，Frank Zappa为L.Shankar制作了solo专辑《Touch Me There》，酸性味儿极重的吉他和键盘使得它变得有点“Zappa化”，Shankar的个人色彩倒显黯淡。这是典型的小提琴在摇滚乐的璀璨年代沦为陪衬的故事。通过翻奏摇滚乐金曲而大出风头的David Garrett被定位在“古典跨界”这一尴尬的风格范畴里。<br>
<br>
而“小提琴摇滚”像是个人造词汇，诞生于艺术家们的玩心和不甘心的碰撞间。被摇滚乐“借去”的小提琴或许也能反过来从摇滚里“借”点东西？比如Jean Luc-Ponty开宗明义的合成器弦乐音色，又或者像英国小提琴家Ed Alleyne-Johnson开创的“Looping-pedal”技术，连接着各色单块，在尖锐的和弦反复中上升回授，使得一把单薄的小提琴也能迸发出John Cale所形容“庞大的乐团式噪音”。<br>
<img alt="" src="http://image.wufazhuce.com/FslZxYvWnGOifXz73427R3e1QJyr?imageView2/2/w/394" style="height:221px; width:394px" /><br>
加拿大的Owen Pallett，狂热的教堂管风琴演奏者之子，是这门技艺的个中好手，演出时有种“one-man-army”般的马戏团趣味。他的小提琴音色连接着会令前辈音乐家们无法理解的复杂软件系统，笔记本电脑和小提琴连成一线，几乎让人捉摸不透他究竟是在演奏还是在编程。他和Arcade Fire的合作是21世纪摇滚乐拥抱小提琴手的典范，在华丽柔和的巴洛克色彩中弦乐自在地找到了属于它的位置，共同孕育了一种高段位的艺术摇滚气质。<br>
<br>
促成这一破局的不是Frank Zappa或者哪位光芒万丈的摇滚明星，也不是小提琴家们的踽踽独行，而是两者互相借力的结果——必须像John Cale所言“以宗教般的狂热”来迎接二者齿轮的契合，尼龙弦和镍弦上闪烁着同样突进不息的能量花火。<br>
