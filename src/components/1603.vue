				<strong>三、林若宁的创作立意</strong><br>
				<br> 他以超脱的智慧关注小人物，为劳苦大众开释；以佛家道家世界观追忆从前，接受现在，融理入情，不讲大道理；也涉及一些边缘题材、禁忌主题，揭开人类虚伪面具。
				<br>
				<br> 他不站在上帝视角，高傲批判。他只是用歌词展现人间万象，作品所触及的社会层面比前辈更广。
				<br>
