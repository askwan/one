				<br> 当时林若宁才小学六年级，他第一次从电台听到这首歌，立即被其中的古典况味吸引，迅速拿出纸笔试图记下歌词。后来当他知道这是受唐涤生影响后，便溯流而上，学习唐涤生粤剧词。
