				<br> 他写过一首《饭岛爱世界》，用大家不敢触碰的AV女优题材来写人的虚伪，也写过《大开色戒》《一代艳星》等与性有关题材，大多数人写性题材歌曲都难逃情色，而林若宁则惯常从情欲延伸到其他更深刻的话题，再如他的成名作《花落谁家》《七百年后》也是从情歌延伸到环保，他的作品总是不那么“单纯”。
				<br>
				<br>
				<strong>四、林若宁的创作风格</strong><br>
				<br>
				<strong>委婉含蓄且柔美典雅，是为婉约。</strong><br>
				<br> 他所创作的歌词中负荷一定人生道理、社会现象的歌词，平铺直叙往往难被人接受，惟幸天生喜欢中国风、古典韵味歌词的林若宁一向注重意境美，即使他已不刻意追求，但融入血液的创作功力随时都在，因此即便不理解词中深意，也能被它的美丽熏染。
				<br>
				<img alt="" src="http://image.wufazhuce.com/FlgRSx505aWkbPb5fmtM8O7uUhVg?imageView2/2/w/394" style="height:262px; width:394px" /><br> 1、风物志
				<br>
				<br>
				<strong>时装的兴替走不过青山<br>
时间过下去都冲淡<br>
难过不过 过那一晚<br>
似潮流过去 回头看一眼<br>
不必轻叹<br>
——《南青山上》</strong><br>
				<br> 林若宁擅用各种修辞方法，尤其是各种明喻暗喻借代虚指等借景抒情、借物言情，借左右而言其他的方法。他喜用地名如《苏州河》《南青山上》《吴哥窟》，喜欢事物如《樱花树下》《黑眼圈》《雨伞》，又喜用人名如《慕容雪》《李莫愁》《宫若梅》。
				<br>
				<br>
				<strong>将想说的想写的灌进美丽风物里，是为婉约的一种。</strong><br>
				<img alt="" src="http://image.wufazhuce.com/FkAy-ZqKCC7fOwSzZHDHmlxY07wd?imageView2/2/w/394" style="height:410px; width:394px" /><br> 2、叙事曲
				<br>
				<br>
				<strong>北方风雪下 绿林白 树摇动<br>
写诗的少年 期望红 无奈穷<br>
——《悔过诗》</strong><br>
				<br> 在研习林夕歌词时，林若宁有两点是经常提起的，一是出外景，二是叙事歌曲的写法他曾在给《飞女正传》的注解里写道：
				<strong>“要知道如何一首歌写一个故事，大可参考此歌词。”</strong><br>
				<br> 歌词篇幅较短，叙事歌词既要做到叙事的完整跌宕，又要保持情感的矛盾层次，林若宁以中国风写叙事词，精简用词的同时美不胜收。
				<br>
				<img alt="" src="http://image.wufazhuce.com/FonivomLRwRtX1CcHQd7gHugbGNx?imageView2/2/w/394" style="height:448px; width:394px" /><br> 3、出外景
				<br>
				<br>
				<strong>泪光装饰夜晚<br>
路灯点缀感叹<br>
列车之上看彼此失散<br>
你面孔早已刻进代官山<br>
——《捞月亮的人》</strong><br>
				<br> “满街脚步，突然静了，满天柏树，突然没有动摇”，《再见二丁目》的这两句歌词最常被引用作为出外景的典范。
				<br>
				<br>
				<strong>所谓出外景，大概就是融情入景、情景结合的意思。</strong><br>
				<br> 对意境美颇有追求的林若宁，对这一写法很有感悟，掌握娴熟，他的词作里常有这种出外景的神奇写法，叫听众一时心旷神怡。
				<br>
				<br>
				<strong>五、低调林若宁</strong><br>
				<img alt="" src="http://image.wufazhuce.com/FhTDbrAx_qP9eNsrZcyMYdLnkffS?imageView2/2/w/394" style="height:233px; width:394px" /><br> ▲ 左一即为林若宁<br>
				<br> 林若宁一向保持低调的作风，网络上鲜有他的照片。直到2009年，他与林夕一起包办李克勤的专辑《Threesome》的歌词，两位填词人被邀拍摄专辑封面，我们才得以见到林若宁的“庐山真面目”。
				<br>
				<br> 正如他自己解读自己是“内敛的人”，他说：“我没有刻意追求婉约，我是个内敛的人，我很少会写一些很直接的东西。日常生活中我很少说话，别人不问便不会讲。所以我写的东西很难直接。”
