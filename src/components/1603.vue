				<br>
				<br> 林若宁也被视为继林夕和黄伟文之后能扛起香港填词人大旗最有希望的年轻词人之一。但他却说：只想过和未来妻子晚饭后散散步的生活。
				<br>
				<br> 谁是林若宁？这个稍显女性化的名字后面是一个怎样的从容淡定的填词人？
				<br>
				<img alt="" src="http://image.wufazhuce.com/FvQpvXustmQU0i3xP34Bt7tSkzss?imageView2/2/w/394" style="height:263px; width:394px" /><br> 1986年，新词人林夕为对抗质疑声，“怒”写《传说》，展现文字功力。“小玉典珠钗，铅华求长埋”，“交杯饮砒霜，泉台谐盟约”。
